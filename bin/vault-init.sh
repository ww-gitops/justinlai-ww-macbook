#!/usr/bin/env bash

# Utility for initializing vault
# Version: 1.0
# Author: Paul Carlton (mailto:paul.carlton@weave.works)

set -euo pipefail

function usage()
{
    echo "usage ${0} [--debug] " >&2
    echo "This script will initialize vault" >&2
}

function args() {
  arg_list=( "$@" )
  arg_count=${#arg_list[@]}
  arg_index=0
  while (( arg_index < arg_count )); do
    case "${arg_list[${arg_index}]}" in
          "--debug") set -x;;
               "-h") usage; exit;;
           "--help") usage; exit;;
               "-?") usage; exit;;
        *) if [ "${arg_list[${arg_index}]:0:2}" == "--" ];then
               echo "invalid argument: ${arg_list[${arg_index}]}" >&2
               usage; exit
           fi;
           break;;
    esac
    (( arg_index+=1 ))
  done
}

args "$@"

SCRIPT_DIR=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )
pushd $SCRIPT_DIR/.. >/dev/null
source .envrc

export VAULT_ADDR="https://vault.kubernetes.docker.internal"
#export VAULT_ADDR="http://10.1.3.39:8200"
#export VAULT_ADDR="http://kubernetes.docker.internal:8200"
if [ "$(vault status --format=json | jq -r '.initialized')" == "true" ]; then
  echo "Vault already initialized"
  exit 0
fi

vault operator init -tls-skip-verify -format=json > resources/.vault-init.json
